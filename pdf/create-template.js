/**
 * create-template.js
 *
 * Generates snap-template.pdf — a blank SNAP application form with labeled
 * boxes at fixed coordinates. Run this once before using fill.js.
 *
 *   node create-template.js
 */

const { PDFDocument, rgb, StandardFonts } = require("pdf-lib");
const fs = require("fs");
const path = require("path");

// Each entry defines one form field.
// y is the bottom edge of the input box (points from page bottom).
// The fill.js file uses the same array — keep them in sync.
const FIELDS = [
  { key: "full_name",         label: "Full Name",            y: 620 },
  { key: "address",           label: "Home Address",          y: 555 },
  { key: "zip_code",          label: "ZIP Code",              y: 490 },
  { key: "dependents",        label: "Number of Dependents",  y: 425 },
  { key: "monthly_income",    label: "Monthly Income ($)",    y: 360 },
  { key: "employment_status", label: "Employment Status",     y: 295 },
];

const PAGE_WIDTH  = 612; // letter width  in points (8.5 in × 72)
const PAGE_HEIGHT = 792; // letter height in points (11 in  × 72)
const MARGIN      = 72;  // 1-inch margin
const FIELD_WIDTH = PAGE_WIDTH - MARGIN * 2; // 468 pts
const BOX_HEIGHT  = 30;

async function createTemplate() {
  const pdfDoc = await PDFDocument.create();
  const page   = pdfDoc.addPage([PAGE_WIDTH, PAGE_HEIGHT]);

  const bold   = await pdfDoc.embedFont(StandardFonts.HelveticaBold);
  const normal = await pdfDoc.embedFont(StandardFonts.Helvetica);
  const gray   = rgb(0.35, 0.35, 0.35);
  const border = rgb(0.60, 0.60, 0.60);

  // ── Title block ──────────────────────────────────────────────────────────
  page.drawText("SNAP Application", {
    x: MARGIN, y: 722,
    size: 22, font: bold, color: rgb(0, 0, 0),
  });
  page.drawText("Supplemental Nutrition Assistance Program  |  Demo Form", {
    x: MARGIN, y: 700,
    size: 10, font: normal, color: gray,
  });
  page.drawLine({
    start: { x: MARGIN, y: 690 },
    end:   { x: PAGE_WIDTH - MARGIN, y: 690 },
    thickness: 0.75, color: border,
  });

  // ── Form fields ───────────────────────────────────────────────────────────
  for (const field of FIELDS) {
    // Label above the box
    page.drawText(field.label, {
      x: MARGIN, y: field.y + BOX_HEIGHT + 4,
      size: 10, font: normal, color: gray,
    });

    // Empty input box
    page.drawRectangle({
      x: MARGIN, y: field.y,
      width: FIELD_WIDTH, height: BOX_HEIGHT,
      borderColor: border, borderWidth: 1,
    });
  }

  // ── Footer ────────────────────────────────────────────────────────────────
  page.drawText(
    "This is a demonstration form generated by Formless. Not for official submission.",
    { x: MARGIN, y: 48, size: 8, font: normal, color: rgb(0.65, 0.65, 0.65) }
  );

  const outPath = path.join(__dirname, "snap-template.pdf");
  fs.writeFileSync(outPath, await pdfDoc.save());
  console.log("Template written to:", outPath);
}

createTemplate().catch((err) => {
  console.error("Failed to create template:", err);
  process.exit(1);
});
